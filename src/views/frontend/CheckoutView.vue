      this.orderData = JSON.parse(orderData);
    }
  },
  beforeUnmount() {
    if (this.doesPostCart) {
      window.sessionStorage.removeItem('orderData');
    } else {
      const orderData = JSON.stringify(this.orderData);
      window.sessionStorage.setItem('orderData', orderData);
    }
  },
};
